public class OppoFunnel {
    private int   recordNum;       //记录数
    private int   revenueNum;      //台数
    private float weightTotalCount; //加权后台数
    private int   firstNum;        //初次接触总数
    private int   needNum;         //弄清需求总数
    private int   displayNum;      //新车展示总数
    private int   driveNum;        //试车总数
    private int   orderNum;        //签订单总数
    private int   priceNum;        //报价总数

    public int getRecordNum() {
        return recordNum;
    }

    public void setRecordNum(int recordNum) {
        this.recordNum = recordNum;
    }

    public int getRevenueNum() {
        return revenueNum;
    }

    public void setRevenueNum(int revenueNum) {
        this.revenueNum = revenueNum;
    }

    public float getWeightTotalCount() {
        return weightTotalCount;
    }

    public void setWeightTotalCount(float weightTotalCount) {
        this.weightTotalCount = weightTotalCount;
    }

    public int getFirstNum() {
        return firstNum;
    }

    public void setFirstNum(int firstNum) {
        this.firstNum = firstNum;
    }

    public int getNeedNum() {
        return needNum;
    }

    public void setNeedNum(int needNum) {
        this.needNum = needNum;
    }

    public int getDisplayNum() {
        return displayNum;
    }

    public void setDisplayNum(int displayNum) {
        this.displayNum = displayNum;
    }

    public int getDriveNum() {
        return driveNum;
    }

    public void setDriveNum(int driveNum) {
        this.driveNum = driveNum;
    }

    public int getOrderNum() {
        return orderNum;
    }

    public void setOrderNum(int orderNum) {
        this.orderNum = orderNum;
    }

    public int getPriceNum() {
        return priceNum;
    }

    public void setPriceNum(int priceNum) {
        this.priceNum = priceNum;
    }

}
